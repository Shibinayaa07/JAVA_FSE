-- Exercise 6: Cursors

DECLARE
    CURSOR GenerateMonthlyStatements IS
        SELECT c.CustomerID, c.Name, t.TransactionID, t.TransactionDate, t.Amount, t.TransactionType
        FROM Customers c
        JOIN Accounts a ON a.CustomerID = c.CustomerID
        JOIN Transactions t ON t.AccountID = a.AccountID
        WHERE TRUNC(t.TransactionDate, 'MM') = TRUNC(SYSDATE, 'MM')
        ORDER BY c.CustomerID, t.TransactionDate;
BEGIN
    FOR statement_record IN GenerateMonthlyStatements LOOP
        DBMS_OUTPUT.PUT_LINE(
            'Customer ' || statement_record.CustomerID || ' - ' || statement_record.Name
            || ': transaction ' || statement_record.TransactionID
            || ', ' || statement_record.TransactionType
            || ', amount ' || statement_record.Amount
        );
    END LOOP;
END;
/

DECLARE
    CURSOR ApplyAnnualFee IS
        SELECT AccountID
        FROM Accounts
        FOR UPDATE;
    v_fee CONSTANT NUMBER := 100;
BEGIN
    FOR account_record IN ApplyAnnualFee LOOP
        UPDATE Accounts
        SET Balance = Balance - v_fee,
            LastModified = SYSDATE
        WHERE CURRENT OF ApplyAnnualFee;

        DBMS_OUTPUT.PUT_LINE('Applied annual fee to account ' || account_record.AccountID);
    END LOOP;
    COMMIT;
END;
/

DECLARE
    CURSOR UpdateLoanInterestRates IS
        SELECT LoanID, LoanAmount
        FROM Loans
        FOR UPDATE;
    v_new_rate NUMBER;
BEGIN
    FOR loan_record IN UpdateLoanInterestRates LOOP
        IF loan_record.LoanAmount >= 10000 THEN
            v_new_rate := 8;
        ELSE
            v_new_rate := 6;
        END IF;

        UPDATE Loans
        SET InterestRate = v_new_rate
        WHERE CURRENT OF UpdateLoanInterestRates;

        DBMS_OUTPUT.PUT_LINE('Updated loan ' || loan_record.LoanID || ' to ' || v_new_rate || '%');
    END LOOP;
    COMMIT;
END;
