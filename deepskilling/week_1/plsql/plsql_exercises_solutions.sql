        VALUES ('AddNewCustomer', 'Customer ID already exists: ' || p_customer_id);
        COMMIT;
        DBMS_OUTPUT.PUT_LINE('Customer insert failed: duplicate customer ID');
    WHEN OTHERS THEN
        ROLLBACK;
        INSERT INTO ErrorLog (ModuleName, ErrorMessage)
        VALUES ('AddNewCustomer', SQLERRM);
        COMMIT;
        DBMS_OUTPUT.PUT_LINE('Customer insert failed: ' || SQLERRM);
END;
