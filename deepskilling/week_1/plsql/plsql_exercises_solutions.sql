DECLARE
    v_discount NUMBER := 1;
BEGIN
    FOR customer_record IN (
        SELECT c.CustomerID
        FROM Customers c
        WHERE FLOOR(MONTHS_BETWEEN(SYSDATE, c.DOB) / 12) > 60
    ) LOOP
        UPDATE Loans
        SET InterestRate = GREATEST(InterestRate - v_discount, 0)
        WHERE CustomerID = customer_record.CustomerID;

        DBMS_OUTPUT.PUT_LINE('Applied senior discount for customer ' || customer_record.CustomerID);
    END LOOP;
    COMMIT;
END;
